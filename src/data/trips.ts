/**
 * Validates the TRIPS dataset to ensure all trip entries have valid properties.
 * Specifically checks for missing/empty key fields required for UI rendering.
 * Logs warnings to the console for any broken records.
 */
export function validateTripImages(): boolean {
  let isValid = true;
  TRIPS.forEach((trip) => {
    if (!trip.image || trip.image.trim() === "") {
      console.warn(`[Trip Validation] Missing image for Trip ID: ${trip.id} - ${trip.title}`);
      isValid = false;
    }
    if (!trip.title || trip.title.trim() === "") {
      console.warn(`[Trip Validation] Missing title for Trip ID: ${trip.id}`);
      isValid = false;
    }
    if (!trip.destination || trip.destination.trim() === "") {
      console.warn(`[Trip Validation] Missing destination for Trip ID: ${trip.id}`);
      isValid = false;
    }
    if (typeof trip.price !== "number" || trip.price < 0) {
      console.warn(`[Trip Validation] Invalid price for Trip ID: ${trip.id}`);
      isValid = false;
    }
    if (typeof trip.rating !== "number" || trip.rating < 0 || trip.rating > 5) {
      console.warn(`[Trip Validation] Invalid rating for Trip ID: ${trip.id}`);
      isValid = false;
    }
  });

  if (isValid) {
    console.log("[Trip Validation] All TRIPS entries are valid and properly structured.");
  }

  return isValid;
}
